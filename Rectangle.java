public class Rectangle {
    private double l,b,area;

    public void calcArea()
    {
        area=l*b;
    }

    public double getL()
    {
        return l;
    }

    public void setL(double l)
    {
        this.l=l;
    }

    public double getB()
    {
        return b;
    }

    public void setB(double b)
    {
        this.b=b;
    }

    public double getArea()
    {
        return area;
    }
}
